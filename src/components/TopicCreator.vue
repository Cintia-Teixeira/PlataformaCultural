<script>
import {
  required,
  minLength,
  maxLength,
  sameAs
} from "vuelidate/lib/validators";
import { mapGetters } from "vuex";
import { createHelpers } from "vuex-map-fields";
import ColorLine from "./ColorLine.vue";
import ProgressBar from "./BaseStepProgressBar.vue";
import BaseButton from "./BaseButton.vue";

const { mapFields } = createHelpers({
  getterType: "topics/getField",
  mutationType: "topics/updateField",
});

const hasCategory = (category) => category !== null;

export default {
  name: "TopicCreator",
  components: {
    ColorLine,
    ProgressBar,
    BaseButton,
  },
  data() {
    return {
      currentStep: 1,
      count: 0,
      numberOfSteps: 3,
      stepsTitle: ["O que s√£o os Debates", "Regras", "Crie seu Debate"],
      rulesAccepted: false,
      rulesError: false,
      terms: false,
      hasSelected: false,
      options: [],
      categoryField: '',
    };
  },
  validations: {
    title: {
      required,
      minLength: minLength(5),
      maxLength: maxLength(100),
    },
    content: {
      required,
      minLength: minLength(5),
      maxLength: maxLength(2000),
    },
    terms: {
      sameAs: sameAs( () => true )
    },
    category: {
      hasCategory
    }
  },
  computed: {
    ...mapFields({
      title: "topicForm.title",
      content: "topicForm.content",
      category: "topicForm.categoryId",
      categoriesTagged: "topicForm.categoriesTagged",
    }),
    ...mapGetters({
      categories: "categories/loadCategories",
    }),
    titleErrorMessage() {
      if (!this.$v.title.required) {
        return "Campo obrigat√≥rio";
      }
      if (!this.$v.title.minLength) {
        return "M√≠nimo de 5 caracteres";
      }
      if (!this.$v.title.maxLength) {
        return "M√°ximo de 100 caracteres";
      }
      return "";
    },
    contentErrorMessage() {
      if (!this.$v.content.required) {
        return "Campo obrigat√≥rio";
      }
      if (!this.$v.content.minLength) {
        return "M√≠nimo de 5 caracteres";
      }
      if (!this.$v.content.maxLength) {
        return "M√≠nimo de 2000 caracteres";
      }
      return "";
    },
    termsErrorMessage() {
      return "√â necess√°rio aceitar os termos";
    },
    categoryErrorMessage() {
      return "√â necess√°rio escolher um tema";
    },
  },
  created() {
    this.options = [...this.categories];
  },
  updated(){
  },
  methods: {
    async submit() {
      // await this.$store.dispatch('topics/createNewTopic')
      // this.$socket.emit('newTopicToServer',  );
      // this.$router.push('/topics');
      this.$v.$touch();
      if(!this.$v.$anyError) {
        this.$store
        .dispatch("topics/createNewTopic", { $socket: this.$socket })
        .then(() => {
          this.content = "";
          this.title = "";
          this.category = null;
          this.categoriesTagged = [];
          this.$router.push("/topics");
        });
      }
      else {
        this.$q.notify({
          message: "Por favor, preencha todos os campos para o debate",
          position: 'top-right',
        });
      }
    },
    cancel() {
      this.content = "";
      this.title = "";
      this.category = null;
      this.categoriesTagged = [];
      this.$router.push({ name: "Topics" });
    },
    tagEvent(sel) {
      if (this.category === null && this.categoriesTagged.length === 0) {
        this.tagMain(sel);
      } else if (this.categoriesTagged.some((tag) => tag === sel)) {

        const index = this.categoriesTagged.findIndex(
          (el) => el.value === sel.value
        );
        const element = this.categoriesTagged[index];
        document
          .getElementById(`icon-${sel.value}`)
          .getElementById("g")
          .setAttribute("fill", `${element.color}`);
        document.getElementById(`category-label-${sel.value}`).style.color =
          "#000";
        document.getElementById(`item-${sel.value}`).style.borderRight = "none";
        this.categoriesTagged.splice(index, 1);

      } else {
        const aux = this.categoriesTagged;
        aux.push(sel);
        this.categoriesTagged = aux;
        document.getElementById(
          `category-label-${sel.value}`
        ).style.color = `${sel.color}`;
        document.getElementById(
          `item-${sel.value}`
        ).style.borderRight = `2px solid ${sel.color}`;
      }
    },
    tagMain(sel) {

      this.category = sel;
      document.getElementById(
        `category-label-${sel.value}`
      ).style.color = `${sel.color}`;
      this.hasSelected = true;
      const index = this.options.findIndex((el) => el.value === sel.value);
      this.options.splice(index, 1);
    },
    untagMain() {
      this.options.push(this.category);
      this.category = null;
    },
    nextStep() {
      if (this.options.length === 0) {
        this.options = [...this.categories];
      }
      if (this.currentStep === 2 && this.rulesAccepted === false) {
        this.rulesError = true;
      } else if (this.count <= this.numberOfSteps) {
        this.currentStep += 1;
        this.count += 1;
        this.scrollToTop();
        this.rulesError = false;
      }
    },
    prevStep() {
      if (this.count >= 0) {
        this.currentStep -= 1;
        this.count -= 1;
      }
      this.scrollToTop();
    },
    scrollToTop() {
      window.scrollTo({
        top: 0,
        left: 0,
        behavior: "smooth",
      });
    },
  },
};
</script>

